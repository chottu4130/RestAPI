package POJOforSeralizationAndDeseralization;

public class Spouse
{
	String name;
	int age;
	
	
	public Spouse(String name, int age) 
	{
		
		this.name = name;
		this.age = age;
	}
	
	public Spouse()
	{
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
	

}
